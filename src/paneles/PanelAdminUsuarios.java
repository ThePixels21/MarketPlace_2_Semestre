    private void btnFiltrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnFiltrarActionPerformed
        // TODO add your handling code here:
        String filtro = txtFiltro.getText();
        Usuario aux1 = controlador.buscar(filtro);
        Usuario aux2 = controlador.buscarId(filtro);
        Usuario aux3 = controlador.buscarCorreo(filtro);
        Usuario aux4 = controlador.buscarTelefono(filtro);
        
        buscarFiltroTabla(aux1, aux2, aux3, aux4);
        if(aux1 != null || aux2 != null || aux3 != null || aux4 != null){
            activarGestion();
        }else{
            restaurarPanel();
        }
    }//GEN-LAST:event_btnFiltrarActionPerformed

    private void btnModificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnModificarActionPerformed
        // TODO add your handling code here:
        String filtro = txtFiltro.getText();
        Usuario aux1 = controlador.buscar(filtro);
        Usuario aux2 = controlador.buscarId(filtro);
        Usuario aux3 = controlador.buscarCorreo(filtro);
        Usuario aux4 = controlador.buscarTelefono(filtro);
        
        if(aux1!=null){
            PanelAdminModificarUsuario panel = new PanelAdminModificarUsuario(aux1);
            mostrarPanel(panel);
        } else if (aux2 != null) {
            PanelAdminModificarUsuario panel = new PanelAdminModificarUsuario(aux2);
            mostrarPanel(panel);
        } else if (aux3 != null) {
            PanelAdminModificarUsuario panel = new PanelAdminModificarUsuario(aux3);
            mostrarPanel(panel);
        } else if (aux4 != null) {
            PanelAdminModificarUsuario panel = new PanelAdminModificarUsuario(aux4);
            mostrarPanel(panel);
        }
    }//GEN-LAST:event_btnModificarActionPerformed

