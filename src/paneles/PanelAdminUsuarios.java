    private void mostrarTabla(){
        crearTabla(controlador.construirMatris());
    }
    private void buscarFiltroTabla(Usuario a, Usuario b, Usuario c, Usuario d) {
        if (a != null) {
            filtrarTabla(a);
        } else if (b != null) {
            filtrarTabla(b);
        } else if (c != null) {
            filtrarTabla(c);
        } else if (d != null) {
            filtrarTabla(d);
        }else if(a == null && b == null && c == null && d == null){
            JOptionPane.showMessageDialog(this, "No se encontr√≥");
        }

    }
    private void filtrarTabla(Usuario u) {
        crearTabla(controlador.contruirMatrisPorUsiario(u));
        
        btnRemoverFiltro.setEnabled(true);
        btnModificar.setEnabled(true);
        btnEliminar.setEnabled(true);
    }
    private void limpiarTxt(){
        txtFiltro.setText("");
    }
    private void mostrarPanel(JPanel p){
        p.setSize(635,194);
        
        panelAbsoluto.removeAll();
        panelAbsoluto.add(p, new AbsoluteConstraints(0,0,-1,-1));
        panelAbsoluto.revalidate();
        panelAbsoluto.repaint();
    }
    private void restaurarPanel(){
        lblUsuariosRegistrados.setText(Integer.toString(controlador.contarUsuarios()));
        mostrarTabla();
        txtFiltro.setVisible(true);
        btnFiltrar.setVisible(true);
        btnModificar.setEnabled(false);
        btnEliminar.setEnabled(false);
        btnRemoverFiltro.setEnabled(false);
        btnModificar.setVisible(false);
        btnEliminar.setVisible(false);
        btnRemoverFiltro.setVisible(false);
        txtFiltro.setText("User, correo, id, tel...");
    }
    private void activarGestion(){
        txtFiltro.setVisible(false);
        btnFiltrar.setVisible(false);
        btnModificar.setEnabled(true);
        btnEliminar.setEnabled(true);
        btnRemoverFiltro.setEnabled(true);
        btnModificar.setVisible(true);
        btnEliminar.setVisible(true);
        btnRemoverFiltro.setVisible(true);
    }
    private void crearTabla(String matris[][]){
        tableUsuarios.setModel(new javax.swing.table.DefaultTableModel(
                matris,
                new String[]{
                    "User", "Correo", "Nombre", "Apellido", "Id", "Edad", "Telefono"
                }
        ) {
            Class[] types = new Class[]{
                java.lang.String.class, java.lang.String.class, java.lang.String.class, java.lang.String.class, java.lang.String.class, java.lang.String.class, java.lang.String.class
            };
            boolean[] canEdit = new boolean[]{
                false, false, false, false, false, false, false
            };

            @Override
            public Class getColumnClass(int columnIndex) {
                return types[columnIndex];
            }

            @Override
            public boolean isCellEditable(int rowIndex, int columnIndex) {
                return canEdit[columnIndex];
            }
        });
    }

