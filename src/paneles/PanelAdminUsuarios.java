    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        // TODO add your handling code here:
        int i = JOptionPane.showConfirmDialog(this, "Est√° seguro de eliminar el usuario?");
        if (i == 0) {
            String filtro = txtFiltro.getText();
            Usuario auxUser = controlador.buscar(filtro);
            Usuario auxId = controlador.buscarId(filtro);
            Usuario auxCorreo = controlador.buscarCorreo(filtro);
            Usuario auxTelefono = controlador.buscarTelefono(filtro);

            if (auxUser != null) {
                controlador.eliminar(auxUser.getUser());
                restaurarPanel();
            } else if (auxId != null) {
                controlador.eliminar(auxId.getUser());
                restaurarPanel();
            } else if (auxCorreo != null) {
                controlador.eliminar(auxCorreo.getUser());
                restaurarPanel();
            } else if (auxTelefono != null) {
                controlador.eliminar(auxTelefono.getUser());
                restaurarPanel();
            }
        }
        
    }//GEN-LAST:event_btnEliminarActionPerformed

