    private void btnModificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnModificarActionPerformed
        // TODO add your handling code here:
        if (txtUser.getText().isEmpty() || txtCorreo.getText().isEmpty() || txtPass.getText().isEmpty()
            || txtNombres.getText().isEmpty() || txtApellidos.getText().isEmpty() || txtDocumento.getText().isEmpty()
            || txtTelefono.getText().isEmpty() || txtEdad.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Por favor llene todos los campos");
        } else {
            if (validLtrs(txtNombres.getText()) && validLtrs(txtApellidos.getText())
                && validNums(txtDocumento.getText()) && validNums(txtTelefono.getText()) && validNums(txtEdad.getText())) {
                String user = txtUser.getText();
                String correo = txtCorreo.getText();
                String psw = txtPass.getText();
                String nombre = txtNombres.getText();
                String apellido = txtApellidos.getText();
                String dni = txtDocumento.getText();
                String telefono = txtTelefono.getText();
                int edad = Integer.parseInt(txtEdad.getText());

                admin.setUser(user);
                admin.setCorreo(correo);
                admin.setDni(dni);
                admin.setTelefono(telefono);
                admin.setPsw(psw);
                admin.setNombre(nombre);
                admin.setApellido(apellido);
                admin.setEdad(edad);

                JOptionPane.showMessageDialog(this, "Datos modificados correctamente");
                PanelAdminUsuarios p = new PanelAdminUsuarios();
                p.setSize(635, 194);

                panelAbsoluto.removeAll();
                panelAbsoluto.add(p, new AbsoluteConstraints(0, 0, -1, -1));
                panelAbsoluto.revalidate();
                panelAbsoluto.repaint();

            } else {
                JOptionPane.showMessageDialog(this, "Por favor ingrese datos correctos para cada casilla");
            }
        }
    }//GEN-LAST:event_btnModificarActionPerformed

    private void setText(){
        txtUser.setText(admin.getUser());
        txtCorreo.setText(admin.getCorreo());
        txtPass.setText(admin.getPsw());
        txtNombres.setText(admin.getNombre());
        txtApellidos.setText(admin.getApellido());
        txtDocumento.setText(admin.getDni());
        txtTelefono.setText(admin.getTelefono());
        txtEdad.setText(Integer.toString(admin.getEdad()));
    }
    private boolean validLtrs(String datos){
        return datos.matches("[a-zA-Z]*");
    }
    private boolean validNums(String datos){
        return datos.matches("[0-9]*");
    }

