    private void btnModificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnModificarActionPerformed
        // TODO add your handling code here:
        String id = txtFiltro.getText();
        Producto aux = controlador.buscar(id);
        if(aux!=null && aux.getUsuario().getUser().equals(usuario.getUser())){
            PanelUsuarioModificarProducto panel = new PanelUsuarioModificarProducto(aux);
            panel.setSize(700, 310);
            
            panelAbsoluto.removeAll();
            panelAbsoluto.add(panel, new AbsoluteConstraints(0, 0, -1, -1));
            panelAbsoluto.revalidate();
            panelAbsoluto.repaint();
        }else{
            JOptionPane.showMessageDialog(this, "Id de producto no encontrada");
        }
    }//GEN-LAST:event_btnModificarActionPerformed

    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        // TODO add your handling code here:
        String id = txtFiltro.getText();
        Producto aux = controlador.buscar(id);
        if(aux!=null && aux.getUsuario().getUser().equals(usuario.getUser())){
            int i = JOptionPane.showConfirmDialog(this, "Está seguro de quitar el producto a la venta?");
            if(i == 0){
                controlador.eliminar(aux.getId());
                lblProductosEnVenta.setText(Integer.toString(controlador.obtenerProductosVentaUsuario(usuario.getUser())));
                mostrarTabla();
            }
        }else{
            JOptionPane.showMessageDialog(this, "Id de producto no encontrada");
        }
    }//GEN-LAST:event_btnEliminarActionPerformed


