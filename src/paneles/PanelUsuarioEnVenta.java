    private void btnInformacionActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnInformacionActionPerformed
        // TODO add your handling code here:
        String id = txtFiltro.getText();
        Producto aux = controlador.buscar(id);
        if(aux!=null && aux.getUsuario().getUser().equals(usuario.getUser())){
            String disponibilidad = "NO";
            if(aux.isDisponible()){
                disponibilidad = "SI";
            }
            JOptionPane.showMessageDialog(this, "Id: "+ aux.getId() + "\nNombre: " + aux.getNombre()
            +"\nDescripcion: "+aux.getDesc() + "\nPrecio: $"+aux.getPrecio() + "\nCategoría: "+aux.getCategoria().getNombre()
            +"\nStock: "+aux.getStock()+"\nDisponible: "+disponibilidad + "\nDueño: " + aux.getUsuario().getUser()
            +"\nFecha publicación: "+aux.getFechaPublicacion());
        }else{
            JOptionPane.showMessageDialog(this, "Id de producto no encontrada");
        }
    }//GEN-LAST:event_btnInformacionActionPerformed

