    private void btnModificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnModificarActionPerformed
        // TODO add your handling code here:
        if(txtNombre.getText().isEmpty() || txtPrecio.getText().isEmpty() || txtDescripcion.getText().isEmpty()
            || txtStock.getText().isEmpty()){
            JOptionPane.showMessageDialog(this, "Por favor llene todos los campos");
        }else{
            if(validNums(txtPrecio.getText()) && validNums(txtStock.getText())){
                String nombre = txtNombre.getText();
                double precio = Double.parseDouble(txtPrecio.getText());
                String descripcion = txtDescripcion.getText();
                int stock = Integer.parseInt(txtStock.getText());
                Categoria categoria = new Categoria(cboxCategoria.getSelectedItem().toString());
                String estado = cboxEstado.getSelectedItem().toString();
                
                producto.setNombre(nombre);
                producto.setPrecio(precio);
                producto.setDesc(descripcion);
                producto.setCategoria(categoria);
                
                if(estado.equals("Disponible")){
                    producto.setDisponible(true);
                }else{
                    producto.setDisponible(false);
                    JOptionPane.showMessageDialog(this, "Producto retirado de la venta por estado NO disponible");
                }
                if (stock >= 0) {
                    producto.setStock(stock);
                } else {
                    JOptionPane.showMessageDialog(this, "No se pudo modificar el stock, ya que debe ser mayor o igual a 0");
                }
                if (stock == 0) {
                    producto.setDisponible(false);
                    JOptionPane.showMessageDialog(this, "Producto retirado de la venta debido a que no tiene unidades disponibles");
                }
                
                btnVolver.doClick();
                
            }else{
                JOptionPane.showMessageDialog(this, "Por favor llene los campos con caracteres correctos");
            }
        }
    }//GEN-LAST:event_btnModificarActionPerformed

