    private void llenarCampos(){
        txtNombre.setText(producto.getNombre());
        txtPrecio.setText(Double.toString(producto.getPrecio()));
        txtDescripcion.setText(producto.getDesc());
        txtStock.setText(Integer.toString(producto.getStock()));
        cboxCategoria.setSelectedItem(producto.getCategoria().getNombre());
        if(producto.isDisponible()){
            cboxEstado.setSelectedIndex(0);
        }else{
            cboxEstado.setSelectedIndex(1);
        }
    }
    private void llenarBoxCategorias(){
        cboxCategoria.setModel(new DefaultComboBoxModel(controlador.obtenerCategorias()));
    }
    private boolean validNums(String datos){
        return datos.matches("[0-9.]*");
    }

