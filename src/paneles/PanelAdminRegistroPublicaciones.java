    private void btnDetallesActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDetallesActionPerformed
        // TODO add your handling code here:
        String id = txtFiltro.getText();
        Producto aux = controlador.buscarPublicacion(id);
        if(aux != null){
            JOptionPane.showMessageDialog(this, "Id: "+ aux.getId() + "\nNombre: " + aux.getNombre()
            +"\nDescripcion: "+aux.getDesc() + "\nPrecio: $"+aux.getPrecio() + "\nCategoría: "+aux.getCategoria().getNombre()
            + "\nVendedor: " + aux.getUsuario().getUser() + "\nFecha publicación: "+aux.getFechaPublicacion());
        }else{
            JOptionPane.showMessageDialog(this, "Id de publicacion no encontrada");
        }
    }//GEN-LAST:event_btnDetallesActionPerformed

