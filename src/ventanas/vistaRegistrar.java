    private boolean validL(String datos){
        return datos.matches("[a-zA-Z]*");
    }
    private boolean validN(String datos){
        return datos.matches("[0-9]*");
    }
    private boolean validarPass(){
        String psw = txtPassword.getText();
        String confrmPsw = txtConfirmPassword.getText();
        return psw.equals(confrmPsw);
    }
    private void msj(String msj){
        JOptionPane.showMessageDialog(this, msj);
    }
