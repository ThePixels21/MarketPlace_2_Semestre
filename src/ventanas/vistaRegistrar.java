    private void btnRegistrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegistrarActionPerformed
        // TODO add your handling code here:
        String user = txtUser.getText();
        String correo = txtCorreo.getText();
        String psw = txtPassword.getText();
        String nombre = txtNombres.getText();
        String apellido = txtApellidos.getText();
        String dni = txtDocumento.getText();
        String telefono = txtTelefono.getText();
        if (user.isEmpty() || correo.isEmpty() || psw.isEmpty() || nombre.isEmpty() || apellido.isEmpty()
                || dni.isEmpty() || telefono.isEmpty()) {
            msj("Por favor llene todos los campos");
        } else {
            if (validL(nombre) && validL(apellido) && validN(dni) && validN(telefono)) {
                int dia = Integer.parseInt(cboxDia.getSelectedItem().toString().trim());
                int mes = Integer.parseInt(cboxMes.getSelectedItem().toString().trim());
                int anio = Integer.parseInt(cboxAnio.getSelectedItem().toString().trim());
                Usuario u = new Usuario(user, correo, psw, nombre, apellido, dni, telefono, Usuario.determinarEdad(dia, mes, anio));
                Usuario aux = controlador.buscar(u.getUser());
                boolean aux2 = controlador.validarUsuario(u);
                if (validarPass()) {
                    if (aux == null) {
                        if (aux2) {
                            boolean respuesta = controlador.registrar(u);
                            if (respuesta) {
                                msj("Se registró satisfactoriamente");
                                btnVolver.doClick();
                            } else {
                                msj("No se pudo registrar");
                            }
                        } else {
                            msj("No se pudo registrar, intente cambiar el correo, el documento o el telefono");
                        }
                    } else {
                        msj("No se pudo registrar, el usuario ya existe");
                    }
                } else {
                    msj("Las dos contraseñas ingresadas no coinciden");
                }
            } else {
                msj("Por favor llene los campos con los caracteres correctos");
            }
        }
    }//GEN-LAST:event_btnRegistrarActionPerformed

